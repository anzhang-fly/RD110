#include "mcp4017.h"
#include "delay.h"

//初始化IIC
void IIC1_Init(void)
{			
    GPIO_InitTypeDef  GPIO_InitStructure;

    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC, ENABLE);//使能GPIOC时钟
    //GPIOC4,C5初始化设置
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_4|GPIO_Pin_5;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;        //普通输出模式
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;		 //IO口速度为50MHz
    GPIO_Init(GPIOC, &GPIO_InitStructure);//初始化
    IIC1_SCL=1;
    IIC1_SDA=1;
}

//产生IIC起始信号
void IIC1_Start(void)
{
	SDA1_OUT();     //sda线输出
	IIC1_SDA=1;	  	  
	IIC1_SCL=1;
	delay_us(4);
 	IIC1_SDA=0;//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	IIC1_SCL=0;//钳住I2C总线，准备发送或接收数据 
}	

//产生IIC停止信号
void IIC1_Stop(void)
{
	SDA1_OUT();//sda线输出
	IIC1_SCL=0;
	IIC1_SDA=0;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	IIC1_SCL=1; 
	IIC1_SDA=1;//发送I2C总线结束信号
	delay_us(4);							   	
}

//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u8 IIC1_Wait_Ack(void)
{
    u8 ucErrTime = 0;	
    SDA1_IN();      //SDA设置为输入   
    IIC1_SDA = 1;
    delay_us(1);	   
    IIC1_SCL = 1;
    delay_us(1);	
	while (READ1_SDA)
	{
        ucErrTime++;
        if (ucErrTime>250)
        {
            IIC1_Stop();
            return 1;
        }
	}
	IIC1_SCL=0;//时钟输出0 	   
	return 0;  
} 

//产生ACK应答
void IIC1_Ack(void)
{
	IIC1_SCL=0;
	SDA1_OUT();
	IIC1_SDA=0;
	delay_us(2);
	IIC1_SCL=1;
	delay_us(2);
	IIC1_SCL=0;
}

//不产生ACK应答		    
void IIC1_NAck(void)
{
	IIC1_SCL=0;
	SDA1_OUT();
	IIC1_SDA=1;
	delay_us(2);
	IIC1_SCL=1;
	delay_us(2);
	IIC1_SCL=0;
}	

//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void IIC1_Send_Byte(u8 txd)
{                        
    u8 t;   
    SDA1_OUT(); 	    
    IIC1_SCL=0;//拉低时钟开始数据传输
    for(t=0; t<8; t++)
    {              
        IIC1_SDA=(txd&0x80)>>7;
        txd <<= 1; 	  
        delay_us(2);   //对TEA5767这三个延时都是必须的
        IIC1_SCL=1;
        delay_us(2); 
        IIC1_SCL=0;	
        delay_us(2);
    }	 
}

//读1个字节，ack=1时，发送ACK，ack=0，发送nACK   
u8 IIC1_Read_Byte(unsigned char ack)
{
	unsigned char i,receive=0;
	SDA1_IN();//SDA设置为输入
    for(i=0;i<8;i++ )
    {
        IIC1_SCL=0; 
        delay_us(2);
        IIC1_SCL=1;
        receive<<=1;
        if(READ1_SDA)
        {
            receive++;
        }   
        delay_us(1); 
    }					 
    if (!ack)
        IIC1_NAck();//发送nACK
    else
        IIC1_Ack(); //发送ACK   
    return receive;
}

void I2C1_init(void)
{
	IIC1_Init();
}

uint16_t MCP4017_Read_IV(void)
{
	uint16_t val;	
	IIC1_Start();           //开启IIC
	IIC1_Send_Byte(0x5f);   //发送读数据信号
	IIC1_Wait_Ack();        //等待响应
	val=IIC1_Read_Byte(0);  //读取数据
	IIC1_Stop();            //停止IIC
	return val;
}

void MCP4017_Write_IV(uint16_t info)
{
	IIC1_Start();           //开启IIC
	IIC1_Send_Byte(0x5e);   //发送写数据信号
	IIC1_Wait_Ack();        //等待响应	
	IIC1_Send_Byte(info);   //发送要写的数据
	IIC1_Wait_Ack();
	IIC1_Stop();            //停止IIC
}

